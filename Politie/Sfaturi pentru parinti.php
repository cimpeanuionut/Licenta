<p>
Criminalitatea este un fenomen social si, la fel cu alte fenomene (social, demografic,
 economic), este alcatuita dintr-o serie de fapte - crimele care au loc in societate,
 in viata si relatia dintre oameni, si care, privite in totalitatea lor, formeaza 
 criminalitatea.
Tinerii interpreteaza adeseori gresit crimele si faptele care pot deveni fapte 
pedepsibile.

Pentru copii se sterg usor granitele dintre faptele pedepsibile si simplele lovituri,
 daca acestea nu le-au fost explicate corect din timp si nu li s-a vorbit clar atat 
 despre urmarile acestor fapte pentru victima, cat si despre consecintele lor pentru
 parinti. In primul rand sunteti dumneavoastra (ca mama sau tata, matusa sau bunic,
 ori pur si simplu prieten) trasi la raspundere.

Multi tineri pot fi tinuti departe de cariera de criminal daca parintii si alte 
persoane care se ocupa de educarea copilului stiu despre ce este vorba si actioneaza
 rapid si eficient pentru a-l opri sa alunece pe o panta gresita.</p>
 
 <p>In acest sens va recomandam cateva sfaturi utile, care speram ca va vor ajuta
 sa va feriti copiii de criminalitate :<p>
 
 <p>- explicati clar copiilor si adolescentilor ca furturile, indiferent cat de 
 nesemnificative sunt nu vor fi probe de curaj ci fapte culpabile;</p>
 
<p>- spuneti-le clar ca, in cazul savarsirii unor furturi, nu va fi vatamat nici un
 magazin sau cumparator ci hotul insusi va trebui sa plateasca pentru faptele sale;</p>
 
<p>- vorbiti cu copiii si adolescentii despre valoarea proprietatii personale si publice
.Spuneti-le ca distrugerile indraznete nu aduc prejudicii doar celor care le fac ci
 si comunitatii;</p>
 
<p>- explicati copiilor si adolescentilor ce fel de pedepse pot primi ca urmare a
 comiterii acestor fapte (de ex. perchezitionarea locuintei, interogarea, 
 masuri de internare intr-un centru de minori);</p>
 
 <p>- vorbiti cu copilul dumneavoastra despre cine ii sunt prietenii si cunostintele
 sale. Intrebati-l apoi unde si cum isi petrece timpul liber in afara de casa;</p>

 <p>- luati seama de cati bani dispune copilul dumneavoastra; testati felul cum
 cheltuie banii de buzunar sau economiile iar in caz ca aveti indoieli stati
 de vorba cu el;</p>
 
<p>- urmariti comportamentul copilului dumneavoastra, observand daca se plimba cu 
o bicicleta pe care dvs. nu i-ati cumparat-o sau daca poseda lucruri neobisnuite 
pentru copii;</p>

<p>- obisnuiti-va copilul mai bine cu exemple din viata de zi cu zi;</p>
<p>- explicati-le ce urmari poate avea distrugerea diferitelor lucruri sau bunuri;</p>
<p>- ganditi-va daca pot face diferenta intre lucrurile bune si rele.</p>
