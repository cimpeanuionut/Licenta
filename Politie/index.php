<h3>Cadru legal</h3>
<p>Cadrul juridic general ce reglementeaza activitatea Politiei Romane este Legea 
nr. 218 din 23 aprilie 2002 privind organizarea si functionarea Politiei Romane.
Noul Statut al politistului, adoptat prin Legea nr. 360/2002, completeaza si detaliaza
dispozitiile privind desfasurarea, in limitele si conform legii, a acestei activitati 
pusa in slujba comunitatii.</p>
<p>Astfel, articolul 1 al acestei legi, statueaza ca politistul este functionar 
public civil, cu statut special, inarmat, ce poarta de regula uniforma si exercita 
atributiile stabilite pentru Politia Romana prin lege, ca institutie specializata a
statului.</p>
<p>Dispozitiile referitoare la activitatea politiei, cuprinse secvential in alte legi
 de organizare si functionare a institutiilor statului, au fost completate cu legi 
 speciale ce vizeaza combaterea unui anumit tip de infractionalitate.</p>
 <p>O deosebita atentie a fost acordata modificarii si completarii Codului Penal 
 si Codului de Procedura Penala, protectiei agentului acoperit, a martorului si a 
 datelor, Legii Administratiei publice locale, a celei privind combaterea crimei 
 organizate, precum si legii privind combaterea infractiunilor comise prin mijloacel
 e tehnologiei de varf.</p>
