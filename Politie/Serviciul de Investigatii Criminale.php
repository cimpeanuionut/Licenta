<center><h3>Serviciul de investigatii criminale</h3></center>

<h1>Imputernicit SEF AL SERVICIULUI - Comisar sef de politie Gheorghe MICU</h1>
<p><b>Serviciul de Investigatii Criminale</b> este structura de specialitate care desfasoara 
activitati investigative si de urmarire penala in scopul prevenirii si combaterii 
infractiunilor indreptate impotriva persoanei, avutului public si privat, autoritatii,
 precum si a celor care aduc atingere unor relatii privind convietuirea sociala, 
 fiind incadrat cu ofiteri si agenti de Politie judiciara cu specializare pe domeniile
 si liniile de munca date in responsabilitate.</p>
 
 <p>Actionand ca un adevarat serviciu public aflat la dispozitia cetateanului, in 
 cursul anului 2014 au fost intensificate masurile si activitatile profesionale de
 aparare a comunitatii, raspunzand intr-o mai mare masura cerintelor populatiei pe
 linia combaterii infractionalitatii, in mod deosebit a celei prin care se aduc
 prejudicii deosebit de grave vietii si integritatii corporale a cetateanului, 
 avutului public si privat, precum si pentru combaterea infractiunilor circumscrise 
in sfera marii criminalitati.</p>

<h1>PRINCIPALELE ATRIBUTII</h1>
<p>- efectueaza activitati operative, investigative si de urmarire penala in baza 
ordonantelor de delegare emise de procurorii din Ministerul Public
 (Parchetul de pe langa Inalta Curte de Casatie si Justitie, Parchetele de pe langa Curtile de Apel, Tribunale si Judecatorii), 
 in vederea identificarii autorilor si solutionarii dosarelor penale privind infractiunile din competenta serviciului;</p>

 <p>- monitorizeaza situatia operativa, dinamica inregistrarii si solutionarii cauzelor
 penale cu autori neidentificati, propune sau dispune masuri, intervine si executa 
 activitati directe la unitatile de politie care se confrunta cu o situatie deosebita 
 in acest domeniu;</p>

 <p>- organizeaza si conduce actiuni operative pe intreg teritoriul judetului Prahova,
 in vederea combaterii unor infractiuni care, prin modul de operare si frecventa, pot
 lua amploare;</p>

 <p>- coordoneaza, indruma si sprijina supravegherea si cercetarea gruparilor 
 infractionale care comit infractiuni ce intra in competenta serviciului, pe raza 
 judetului Prahova, la nivel national ori cu elemente de extraneitate cu moduri de
 operare deosebite si prejudicii mari;</p>
 
 <p>-executa direct, coordoneaza, sprijina si evalueaza modul in care s-au efectuat 
 activitatile de cercetare la fata locului, operative si de urmarire penala pentru 
 identificarea autorilor in cauzele privind:</p>
 <i>

 <p> 	infractiuni contra vietii, sanatatii si integritatii corporale;

 <p>  infractiunile la viata sexuala cu accent pe abuzurile sexuale asupra copiilor
 sau a celor in care sunt implicati cetateni straini in calitate de autori;</p>

 <p>  talhariile si furturile comise cu moduri de operare deosebite sau cu prejudicii
 mari;</p>

  <p> traficul ilegal intern si international cu autovehicule, furturile deosebite 
  de si din autovehicule comise pe teritoriul Romaniei;</p>

  <p> falsurile si inselaciunile urmate de consecinte grave, cu exceptia celor date 
  in competenta altor structuri;</p>

 <p> faptele care lezeaza patrimoniul cultural national cu prejudicii mari;</p>

<p>  faptele savarsite de catre organizatii, secte ori grupari cu caracter religios 
si nationalist-sovin.</p>
</i>

<p>- coopereaza cu Centrul de Cooperare Politieneasca Internationala pentru verificarea situatiei bunurilor culturale indisponibilizate de autoritatile altor state;</p>

<p>- organizeaza, coordoneaza si desfasoara nemijlocit activitati de punere in executare a mandatelor si sentintelor penale, emise de autoritatile judiciare;</p>

<p>- organizeaza, coordoneaza si desfasoara nemijlocit activitati de localizare si prindere a urmaritilor la nivel judetean, national si international;</p>

<p>- efectueaza activitatile de testare a persoanelor interesate pentru dobandirea calitatii de detectiv particular si elibereaza atestate de detectiv particular; </p>

<p>- elibereaza autorizatii pentru detinerea/comercializarea detectoarelor de metale;</p>

<p>- elibereaza avize, in conditiile legii, pentru organizarea si functionarea centrelor de dezmembrari autovehicule;  </p>

<p>- gestioneaza evidenta persoanelor liberate din penitenciare si intocmeste informari catre subunitatile de politie teritoriale referitor la persoanele din aceasta categorie comunicate de Administratia Nationala a Penitenciarelor.</p>


