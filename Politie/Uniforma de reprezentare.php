<center><h3>Uniforma de reprezentare</h3></center>

<p>Este de culoare bleumarin - indigo si se poarta atat de catre ofiterii, cat si de
 catre agentii de politie pe timpul deplasarilor de la domiciliu la serviciu, in 
 birouri, la unele intalniri sau la ordinul sefilor si se compune din: sapca, sacou,
 pantaloni, camasa, camasa bluza, scurta imblanita, impermeabil, pantofi sau ghete.</p>

<p>La aceasta uniforma se poarta camasa de culoare gri-bleu cu cravata bleumarin cu 
dungi bleu. Pe timpul verii, se poate purta, fara sacou si cravata, camasa bluza 
cu maneca lunga sau cea cu maneca scurta prevazute cu epoleti.</p>

<p>Ofiterii care nu au drept de port al uniformei de ceremonie, precum si agentii de 
politie, pot purta la decizia sefilor cu ocazia unor ceremonialuri, aniversari,
 spectacole, sarbatori legale etc, uniforma de reprezentare cu camasa alba si eghilet
 sau snur pentru ceremonie.</p>

<p>Insemnele de clasa profesionala sub forma de benzi pe manecile sacoului, petlitele 
sau benzile de pe cozorocul sepcilor, sunt aplicate pe elementele uniformei conform
 modelului stabilit pentru clasa profesionala din care face parte fiecare ofiter, sau
 agent care are drept de port al acestei uniforme.</p>

<p>Insemnele de grad profesional sunt materializate prin epoleti, pe care sunt brodate
 stele de diferite marimi potrivit clasei profesionale din care fac parte ofiterii 
 si barete pentru corpul agentilor, care se monteaza pe umerii sacoului sau sacoului
 de reprezentare, sau se aplica pe epoletii celorlalte articole de echipament din
 compunerea acestei uniforme.</p>
 
