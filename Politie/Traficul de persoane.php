  <P>In cazul in care vreti sa incheiati un contract cu o firma si acel contract este redactat intr-o limba straina, traduceti intai contractul, astfel incat sa-l puteti studia cu atentie. </P>
  <P>Comunicati rudelor, prietenilor adresa locului in care urmeaza sa lucrati. Solicitati numarul de telefon al respectivei firme si cereti cuiva sa va sune acolo dupa un timp.</P>
