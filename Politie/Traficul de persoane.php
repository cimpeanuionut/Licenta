<center><h3>Traficul de persoane</h3></center>

 <p><b>Ce este traficul de persoane? </b></p>
 
     <p>Traficul de persoane inseamna recrutarea, transportarea, adapostirea si vinderea
	 unei persoane in scopul exploatarii fizice. Aceeasi persoana(victima) poate fi
	 vanduta de mai multe ori fara ca ea sa-si dea acordul si de cele mai multe ori 
	 fara sa stie (cel putin in etapele initiale ale traseului pe care il parcurge).
	 Tehnicile de recrutare si modalitatile prin care se cade in plasa unei retele de 
	 trafic de persoane variaza de la simpla inselaciune pana la rapire, sechestrare 
	 si chiar folosirea violentei extreme. </p>
     <p>De retinut: victimele nu primesc nici un beneficiu in urma serviciilor obligate 
	 sa le presteze.</p>
     <b><p>Ce se intelege prin exploatarea unei persoane? </p></b>
     <p>- obligarea la practicarea prostitutiei sau alte forme de exploatare sexuala; </p>
     <p>- executarea unei munci sau indeplinirea de servicii, in mod fortat, cu incalcarea normelor legale privind conditiile de munca, salarizare, sanatate si securitate; </p>
    <p> - tinerea in stare de sclavie sau alte procedee asemanatoare (lipsire de libertate, sechestrare);</p>
     <p>- prelevarea de organe;</p>
    <p>- efectuarea unor alte asemenea activitati prin care se incalca drepturi si libertati fundamentale ale omului.</p>
 
 <p><b>Cum se ajunge in plasa traficantilor? </p></b>

    <p> Dorinta acuta si decizia de a pleca din tara pentru depasirea dificultatilor 
	 economico - financiare. 
     Intalnirea unei ocazii de a pleca in strainatate pentru a lucra, concretizata 
	 printr-o oferta din partea unei persoane cu rol de recrutor al viitoarei victime. 
     Constrangerea persoanei in cauza sa accepte un alt tip de munca, decat cel
	 promis, initial prin amenintare fizica sau referiri la situatia in care se
	 afla victima (trecerea frauduloasa a frontierei, necunoasterea zonei, a limbii,
	 lipsa actelor de identitate, a mijloacelor materiale, etc.) iar apoi prin
	 modalitati de constrangere din ce in ce mai violente. <p>
	 
	  <p><b>Cine si cum intermediaza traficul? </p></b>
 
     <p>Recrutorul poate fi independent sau o firma cu profil aparent de plasare
	 a fortei de munca in strainatate (exemplul unor firme de impresariat artistic). 
     Aproximativ 70% dintre recrutori sunt barbati cu varsta cuprinsa intre 20 si
	 30 de ani, alteori mai in varsta. Femeile recrutoare au varsta cuprinsa intre
	 18 si 35 de ani. Exista cupluri sau chiar familii unde ambii parteneri se ocupa
	 de recrutarea si transportul victimelor. </p>

	 <p><b>Profilul traficantului</b></p>

     <p>- persoana cunoscuta sau chiar ruda care inspira incredere;</p>
     <p>- are o slujba in care exista posibilitatea unui contact intens cu publicul (barman, ospatar, taximetrist, vanzator, etc.);</p>
     <p>- aspectul fizic este de natura sa inspire incredere;</p>
     <p>- situatia materiala a recrutorului - cunoscuta ca buna;</p>
     <p>- atractia in vederea recrutarii este reprezentata de oferte de locuri de munca in strainatate (ospatari, bucatari, dansatoare, etc.);</p>
    <p> - propunerile sunt facute prin intermediul rudelor, cunostintelor sau prin articole in ziare;</p>
  <p>- castiga increderea prin promisiuni si povesti de succes (persoane care au o situatie foarte buna ca urmare a faptului ca au lucrat in strainatate); eventual se asigura cazarea, transportul si hrana pana la destinatie.</p>

   <p><b>Profilul persoanei vulnerabile la trafic</p></b>

     <p>- crede cu tarie in modelele de succes, ca in strainatate nu exista esec;</p>
     <p>- tinere ce au varsta intre 15 si 25 ani;</p>
     <p>- au o imensa dorinta de independenta materiala;</p>
     <p>- isi asuma cu usurinta riscul deciziei de a pleca in strainatate;</p>
     <p>- provin, in general, din zone defavorizate economic;</p>
     <p>- au facut parte din medii sau familii in care au suferit diverse abuzuri;</p>
      <p>- in general, au un nivel de educatie scazut si nu au experienta de viata.</p>
