 <h1>ATRIBUTII PRINCIPALE:</h1>
 
 <p>- realizeaza coordonarea operationala a structurilor de ordine publica subordonate;</p>
 
<p>- asigura implementarea pe plan local a documentelor de planificare strategica cu aplicabilitate in domeniul ordinii publice, emise de la nivel central (strategii, programe si planuri de actiuni pentru implementarea acestora etc.);</p>

<p>- asigura implementarea pe plan local a conceptiilor de organizare/reorganizare a activitatii de ordine publica, emise de la nivel central, si formuleaza propuneri de imbunatatire, modificare si/sau completare, in functie de concluziile rezultate din procesul de implementare;</p>

<p>- asigura punerea in aplicare a programelor si planurilor de actiuni si masuri, emise de la nivel central, care vizeaza prevenirea si combaterea infractionalitatii din competenta sau determina implicarea structurilor de ordine publica teritoriale;</p>

<p>- participa in grupul de lucru constituit la nivelul inspectoratului de politie judetean pentru elaborarea/actualizarea Planului unic de ordine si siguranta publica, precum si in Grupul de Coordonare si Trasare a Sarcinilor;</p>

<p>- participa la organizarea si desfasurarea misiunilor de asigurare a ordinii si linistii publice la manifestatiile publice cu grad ridicat de risc si asigura coordonarea nemijlocita a efectivelor de ordine publica;</p>

<p>- coopereaza/relationeaza cu reprezentantii structurilor teritoriale ale Jandarmeriei Romane, Politiei de Frontiera Romane, Politiei Locale, organele administratiei publice locale, societati specializate de paza etc., pentru prevenirea infractiunilor stradale in sistem integrat;</p>
 
<p>- realizeaza analize periodice cu privire la evolutia criminalitatii din domeniile de competenta, in special criminalitatea stradala, iar in functie de concluziile desprinse, stabileste masurile necesare pentru cresterea eficientei si eficacitatii in prevenirea si combaterea acesteia;</p>

<p>- acorda sprijin,indruma si controleaza modul in care se respecta dispozitiile legale cu privire la paza obiectivelor, bunurilor, valorilor si protectia persoanelor de catre unitati si prestatori de servicii;</p>

<p>- desfasoara nemijlocit, prin personalul desemnat, activitati specifice pentru prevenirea si combaterea braconajului piscicol, a taierilor si transporturilor ilegale de masa lemnoasa din fondul forestier de stat si privat, in zona administrativ-teritoriala a judetulu</p>



