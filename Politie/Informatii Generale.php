<center><h3>Scurt Istoric</h3>
<IMG SRC="Image/Istoric.jpg" width="400" height="200"/></center>
 <p> Prin legea de organizare a politiei din anul 1929 se stabilesc pentru prima data
  norme generale de procedura in materie de politie judiciara. In ordinul 
  nr. 57891/1929, transmis de Directia Politiei Generale, Chesturilor de Politie din
  fiecare resedinta de judet, prin care se anunta infiintarea Directiei Politiei
  Judiciare, erau definite atributiile Politiei Judiciare. </p> 

<p>Rolul Politiei Judiciare incepe cand cel al politiei administrative si de infractiuni
 a incetat. Ea constata in chip cat mai amanuntit infractiunea, urmareste pe faptasi 
 si ii trimite inaintea instantelor judecatoresti pentru sanctionare. Prin urmare, se
 intelege actul material prin care ofiterul de politie judiciara ia toate masurile si
 intrebuinteaza toate mijloacele pentru ca sa ajunga la descoperirea si prinderea
 faptuitorului. Pentru a ajunge acest scop, el se informeaza, supravegheaza si 
 incunostiinteaza toate autoritatile asupra individului sau a imprejurarilor in care
 s-a comis infractiunea al carei autor este necunoscut inca.</P>

<p>Politia Judiciara mai exercita si masurile ordonate de justitie ca: arestari,
 descinderi, perchezitii domiciliare, urmariri etc.</p>
 
 <p>Prin Decizia nr. 57.142 din 1 noiembrie 1929, Chestorul Politiei Municipiului 
 Ploiesti, Constantin Vasilescu, stabilea, in conformitate cu prevederile noii legi,
 organizarea institutiei in trei birouri centrale: Biroul Administrativ, Biroul 
 Judiciar si cel al Sigurantei si Servicii exterioare: comisariate de politie din 
 oras si cele din orasele neresedinta de judet. Biroul Politiei Judiciare urma sa 
 coordoneze patru sectii: Cercetari si Urmariri, Executarea Mandatelor de Depunere
 si Arestare, Identificare (fotograf) si Fise si Cazier.</p>

 <p>Ofiterii din Politia prahoveana, considerati cu experienta si versati in anchete
 judiciare, pana la infiintarea Biroului Judiciar erau: chestorul Vasilescu
 Constantin, comisarul Vieru Constantin  secretarul Chesturii, comisarul 
 Draghicescu Ion - seful Biroului de Politie Administrativa, comisarul Popescu
 Alexandru seful Biroului de Informatii, comisarul Ionescu Andrei seful Biroului
 de Politie Judiciara, comisarul Ionescu Virgil  seful Circumscriptiei II 
 (astazi sectie) Ploiesti, comisarul Stancu Ion din cadrul Biroului de Informatii,
 comisarul Gergescu Petre din cadrul Biroului Judiciar, comisarul Enachescu Ctin 
 seful Comisariatului de Politie Campina, comisarul Diaconescu Teodor  seful 
 Comisariatului de Politie Sinaia, comisarul Pantazopol Dumitru 
 seful Comisariatului de Politie Slanic, comisarul adjunct Bucataru Mihail de la 
 Biroul Judiciar, comisarul adjunct Radulescu Mihail  seful Circumscriptiei I de
 Politie Ploiesti, comisarul adjunct Stanciu Sebastian  seful Detasamentului de
 Politie Urlati.</p>
 
 <p>In ordinul de serviciu din 6 noiembrie 1929 al Chestorului  Politiei Municipiului
 Ploiesti se aduceau anumite clarificari privind desfasurarea unor activitati in
 cadrul Biroului Judiciar. Se cerea astfel ca mandatele de arestare sa fie o 
 preocupare de prim ordin pentru birou. Nu era admisa nicio intarziere in 
 executarea lor sau restituirea lor fara o serioasa cercetare. Mandatele de 
 arestare cu drept de a se incasa amenzile urmau sa aiba un curs rapid. Ofiterul
 de politie insarcinat cu executarea acestora trebuia supravegheat de seful biroului,
 prezentandu-i-se acestuia recipisele de plata a banilor de la Administratia
 Financiara. Saptamanal, seful biroului avea obligatia sa prezinte conducerii
 chesturii situatia mandatelor primite si natura lor, numarul celor executate,
 suma de bani justificata prin recipisele de varsare a banilor la Administratia 
 Financiara, precum si numarul mandatelor neexecutate. </P>

<p> De asemenea, zilnic, biroul trebuia sa efectueze controlul arestului politiei,
 verificand foaia de arest. Tot in acest ordin se specifica faptul ca nu se putea
 retine in arest mai mult de 24 de ore nicio persoana, fara avizul Parchetului. In
 aceasta privinta, abaterile erau considerate abuz de putere, urmand sa fie luate
 cele mai severe masuri, cazurile depistate fiind raportate Ministerului.</p>
 
 <p>La infiintare, Biroul Judiciar era condus de comisarul Ionescu Andrei, care avea
 in subordine  patru comisari adjuncti, doi agenti judiciari, cinci gardieni si un
 impiegat.</p>
 <p>Din documentele de arhiva rezulta ca  Biroul Judiciar al Chesturii Politiei
 Ploiesti s-a confruntat in permanenta cu situatia de a avea un numar insuficient
 de personal, raportat la numarul lucrarilor primite pentru solutionare de acest
 birou. Pe langa executarea mandatelor de arestare, urmarirea diferitilor indivizi 
 ceruti de alte autoritati, intocmirea cazierelor si fiselor, tinerea la curent a 
 arhivei, razii, supravegheri si informatii, zilnic biroului i se repartizau pentru
 cercetari circa 30 de reclamatii, iar pentru fiecare reclamatie in parte erau
 audiati  un reclamant, un inculpat si circa 2-5 martori.</p>
 
 <p>Pentru reducerea furturilor, propunea introducerea serviciului de patrulare in 
 timpul noptii, care sa fie facut pe raza fiecarei circumscriptii (sectie de politie) 
 de la ora 22 pana la 6 dimineata de catre doua patrule, compuse fiecare din doi
 gardieni destinati Biroului Judiciar, cu cate patru jandarmi de la Legiunea Mobila 
 a orasului. De asemenea, solicita infiintarea unui corp de garda din gardieni si 
 jandarmi pentru paza arestului si transferarea la autoritati a delincventilor, 
 activitati ce se desfasurau defectuos in cadrul biroului din lipsa personalului.</p>
 
