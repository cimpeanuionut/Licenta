<h1> Despre Oras </h1>
<p>		Baicoi este un oras in judetul Prahova, Muntenia, Romania, format din localitatile
  componente: Baicoi , Dambu, Liliesti, Schela, Tufeni si Tintea.
  Orasul Baicoi este situat in centrul judetului Prahova. Inaltimea maxima (406 metri)
  raportata la nivelul marii, se atinge in cadrul orasului Dealul Tintea. Orasul se intinde 
  pe o lungime de 17 km. Este dezvoltat practic de-a lungul arterei principale (Strada Republicii)
  din care pleaca mai multe strazi, care depasesc foarte rar lungimea de 2-3 km. Orasul este 
  strabatut, prin zona sa de sud-vest, in afara localitatilor, de soseaua nationala DN1, care leaga 
  Ploiestiul de Brasov. Accesul din aceasta sosea catre localitatile orasului se face prin soselele
  judetene DJ100F, care parcurge orasul de la vest la est, ducand catre Plopeni, si DJ215, care duce mai
  departe spre Scorteni. Din DJ100F, in nord-vestul orasului, porneste si soseaua DJ100E, care duce catre
  Scorteni, Telega, Campina (unde se intersecteaza cu DN1), Provita de Jos, Provita de Sus si Adunati.</p>
		<p>Numele localitații se pare că deriva din Bai (de la Baiu, un cioban coborat din Munții Baiului,
aflați puțin mai la nord de Băicoi - probabil primul locuitor al satului) și köy 
(care în cumană și turcă înseamnă sat). Toponimul Băicoi poate avea deci semnificația 
„satul lui Baiu”. Baiu (ulterior Baicu) a fost familia care a stăpânit cea mai mare parte a  
vetrei satului.	</p>
<p>Stema orașului Băicoi este un scut despicat. Prima parte, tăiată, cuprinde sus, în câmp verde, 
 trei lacrimi de argint; în câmp inferior, pe azur, se află o acvilă bicefală ieșindă de argint.
 În partea din stânga, pe azur, sunt trei brâuri negre. Scutul este timbrat cu o coroană murală 
 de argint cu trei turnuri crenelate (coroana murală cu trei turnuri crenelate semnifică faptul 
 că localitatea are rangul de oraș).
Semnificația elementelor este:
Lacrimile de argint și acvila bicefală de argint ieșindă fac aluzie la familiile boierești Ghica și Cantacuzino, familii care au deținut proprietăți în localitate. Din aceste familii se trage prințesa Cleopatra Trubetzkoi, care a deținut în Băicoi un palat și care a contribuit la ridicarea spirituală și culturală a localității prin ctitorirea unei biserici și prin înființarea unei școli.
Brâurile negre simbolizează țițeiul, localitatea fiind cunoscută prin activitatea de extracție a țițeiului.
Coroana murală, de argint, cu trei turnuri crenelate, semnifică faptul că localitatea are rangul de oraș.</p>
 <p>Atestarea documentară a orașului datează din 20 mai 1597, când Manole și Necula Logofăt au 
 primit de la domnitorul muntean Mihai Viteazul moșia Băicoiului în schimbul moșiei Ploieștilor,
 unde se afla un sat moșenesc pe care voievodul a fondat ulterior un oraș. Comuna Băicoi a fost
 declarată oraș în 1948, imediat după al Doilea Război Mondial. După reorganizarea administrativă
 din 1950, orașul a făcut parte din raionul Câmpina al regiunii Prahova și apoi al regiunii 
 Ploiești. Regiunile au fost desființate în 1968, atunci orașul înglobând comuna Țintea cu toate
 satele ei și fiind arondat județului Prahova.
</p>
